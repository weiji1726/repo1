public class Animal{
    int id;
    String name;
    String eye;
    String feet;

    public int getId(){
        return id;
    }

    public void setId(int id){
        this.id = id;
    }

    public String getName(){
        return name;
    }

    public void setName(String name){
        this.name = name;
    }

    public String getEye(){
        return eye;
    }

    public void setEye(String eye){
        this.eye = eye;
    }

    public String getFeet(){
        return feet;
    }

    public void setFeet(String feet){
        this.feet = feet;
    }
}

